# We defined a variable language and gave it a value
# now if we want to print only a letter from this value. we have to give index for that particular letter.
# [ ] square brackects are used for indexing number in python always

language = "Nitish "
print(language [0])
print(language [1])
print(language [2])

# if we dont know the length of the word and we need to print last letters of that word. we can use -ve indexing from end of the word. For example

print(language [-1])
print(language [-2])
print(language [-3])
print(language [-4])
print(language [-5])
print(language[-6])
print(language[-7])



