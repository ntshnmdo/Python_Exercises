# name += "sh"
name = "niti"   
name += "sh"  # we want to add 'sh' in 'niti', notice that we are not changing string rather we are adding another string to existing string.
print (name)

# we can also use += function for numbers.
age = 25 # using number under "" make it string. therefore we will not use "" for numbers.
age += 1
print(age)

# we can also subtraction, multiple *= using this function,
age_1 = 25
age_1 *= 2
print(age_1)