#To take input from user we use input function,
# Input func always takes input from user in str func.

number_one = int(input("enter first number ")) # 4
number_two = int(input("enter second number ")) # 4
total = number_one + number_two 
print("total is " + str(total))

# total = "4" + "4" = 44 This is taking 4 as a string that's why 4 and 4 are different. and on print it gives total as "4" + "4" = 44

# string func changes NUMBER to string func Ex str 4 ----> "4"

# int func changes input STRING into Integer and number will no longer be string "4" ----> 4 

# float - changes String in to decimal "4" ----> 4.0
