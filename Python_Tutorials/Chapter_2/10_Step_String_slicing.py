# Its not imp to write or define a variable, we can directly take a string
print("Nitish"[0:4]) # [start argument : end argument+1]

# For step slicing syntax- ("string"[::step]) # It will print whole word but skiping letter from starting argument
print("Nitish" [::2]) #skipping 2 letters.
print("Nitish" [::1]) # it will print the same thing.
print("Nitish" [::-1]) # therefore it will print Nitish backwards
#-ve in the step, print the word backwards 